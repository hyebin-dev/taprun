package taprun.core;

public class Utils {
	
	//주사위 숫자(1~6)에 맞는 정사각형 모양의 도트 아트(ASCII 아트) 반환
    public static String getDiceArt(int num) {
        switch (num) {
            case 1:
                return "┌───────────┐\n" +
                       "│           │\n" +
                       "│     ●     │\n" +
                       "│           │\n" +
                       "└───────────┘";
            case 2:
                return "┌───────────┐\n" +
                       "│  ●        │\n" +
                       "│           │\n" +
                       "│        ●  │\n" +
                       "└───────────┘";
            case 3:
                return "┌───────────┐\n" +
                       "│  ●        │\n" +
                       "│     ●     │\n" +
                       "│        ●  │\n" +
                       "└───────────┘";
            case 4:
                return "┌───────────┐\n" +
                       "│  ●     ●  │\n" +
                       "│           │\n" +
                       "│  ●     ●  │\n" +
                       "└───────────┘";
            case 5:
                return "┌───────────┐\n" +
                       "│  ●     ●  │\n" +
                       "│     ●     │\n" +
                       "│  ●     ●  │\n" +
                       "└───────────┘";
            case 6:
                return "┌───────────┐\n" +
                       "│  ●     ●  │\n" +
                       "│  ●     ●  │\n" +
                       "│  ●     ●  │\n" +
                       "└───────────┘";
            default:
                return "[잘못된 주사위]";
        }
    }

    // 몬스터 이름에 맞는 깔끔한 아트 반환
    public static String getMonsterArt(String monsterName) {
        switch (monsterName) {
            case "박쥐":
                return "    \\_.,_/    \n" +
                       "     (o,o)     \n" +
                       "   ./) _ (\\.  \n" ;
            case "슬라임":
                return "    _.-'-._    \n" +
                       "  //'-_ _'-'\\ \n" +
                       "   | a _ a |   \n" +
                       "   |  '-'  |   \n" +
                       "   │ \\ // │   \n" +
                       "   '-.---.-'   \n" ;
            case "버섯":
                return "    /--'\\\\   \n" +
                       "    |o o o|    \n" +
                       "    '.___.'    \n" ;
            case "고블린":
                return "    /\\_ /\\    \n" +
                       "    ( o.o )     \n" +
                       "     > ^ <      \n" +
                       "     // \\      \n" +
                       "    //| |\\     \n" +
                       "   // | | \\    \n" +
                       "  //  | |  \\   \n" ;
            case "구울":
                return "      .-.       \n" +
                       "    //o.o\\     \n" +
                       "    |\\_//|     \n" +
                       "   ///-'\\\\    \n" +
                       "  (.// \\\\.)   \n" ;
            case "스켈레톤":
                return "      .-.       \n" +
                       "     (o.o)      \n" +
                       "      |=|       \n" +
                       "    __|=|____   \n" +
                       "   //.=|=.\\\\  \n" +
                       "  // .=|=. \\\\ \n" +
                       "  \\ .=|=. //// \n" ;
            case "미믹":
                return "   ___________   \n" +
                       "  /\\ /\\        \n" +
                       " / \\_______/ \\ \n" +
                       " | ,-------,  |│ \n" +
                       " | | O | |    ││ \n" +
                       " | |_______|  ││ \n" +
                       " \\ \\VVVVVVV//  \n" +
                       "  \\ /           \n" +
                       "   '---------'   \n" ;
            case "오크":
                return "      .--.       \n" +
                       "     /., \\      \n" +
                       "     |;.. |      \n" +
                       "      -' '       \n" +
                       "     //  \\      \n" +
                       "    | O O  |     \n" +
                       "    | '--' |     \n" +
                       "   \\ v--v //    \n" ;
            case "와이번":
                return "   \\ /  \\\\ /  \n" +
                       "  ( -\\,'--\\/   \n" +
                       "   .)/\\ ,'(\\   \n" +
                       "    / /\\ \\ \\  \n " +
                       "  | , / \\ )  \\ \n" ;
            case "골렘":
                return "     _____      \n" +
                       "   __|o o|__    \n" +
                       "  | |       |   \n" +
                       "  |_________|   \n" +
                       "     | | |      \n" ;
            default:
                return "┌─────────────┐\n" +
                       "│  ??? 몬스터   │ \n" +
                       "│             │\n" +
                       "│    (o.o)    │\n" +
                       "│             │\n" +
                       "└─────────────┘";
        }
    }
}